export function getRegisteredFunction(schema, name) {
  return functionRegistry[schema]?.[name] ?? null;
}

export function listRegisteredFunctions() {
  return Object.entries(functionRegistry).flatMap(([schema, functions]) =>
    Object.entries(functions).map(([name, metadata]) => ({
      schema,
      name,
      qualifiedName: `${schema}.${name}`,
      roles: metadata.roles,
      description: metadata.description,
    })),
  );
}
